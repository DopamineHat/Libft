#include "ft_libft.h"

int		ft_strncmp(const char *s1, const char *s2, size_t n)
{
	while (((unsigned char)*s1++ == (unsigned char)*s2++) && (n-- > 0))
		;
	return (*--s1 - *--s2);
}
